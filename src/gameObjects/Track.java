package gameObjects;

public class Track {

	LocationNode start;
	LocationNode end;
	int length;
	
	public void setStartNode(LocationNode start) {
		this.start = start;
	}
	
	public void setEndNode(LocationNode end) {
		this.end = end;
	}
	
	public void setLength(int length) {
		this.length = length;
	}
	
	
}
