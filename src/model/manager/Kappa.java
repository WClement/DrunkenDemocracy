package model.manager;

public class Kappa {

	int x;
	int y;
	
	String location = "Images/Kappa.png";
	
	public Kappa(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public String getLocation() {
		return location;
	}
	
	
}
